package application;

public class Mostrando_Matizes {

	public static void main(String[] args) {
		
		String[][] matrizA = {{"a", "b", "c"}, {"d", "e", "f"}};
		
		
		
		for(int i=0; i<matrizA.length; i++) {
			
			for(int j=0; j<matrizA[i].length; j++) {
				System.out.print(matrizA[i][j]);
				
			}
			System.out.println();
		}
	
	
	}
		
		
		
	}


